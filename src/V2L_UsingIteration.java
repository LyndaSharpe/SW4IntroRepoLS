public class V2L_UsingIteration {
    public static void main(String[] args) {
        int num1 = 1;
        System.out.println("Loop will stop when num1 = 10");

        while (num1 < 10) {
            System.out.println("Condition not met, numl is " + num1);
            num1++;

        }
        System.out.println("Condition met, num1 is " + num1);
    }
}
